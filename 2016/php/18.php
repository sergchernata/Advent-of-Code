    function count_neighbors_on($row, $col, $grid)
    {
        $lights = '';

        for ($x=$row-1; $x < $row+2; $x++)
        { 
            for ($y=$col-1; $y < $col+2; $y++)
            {
                $current = $x == $row && $y == $col;
                $light_exists = isset($grid[$x][$y]);
                $lights .= !$current && $light_exists ? $grid[$x][$y] : '';
            }
        }

        return substr_count($lights, '#');
    }

