                $top_left = $i == 0 && $k == 0;
                $top_right = $i == 0 && $k == count($grid[0]) - 1;
                $bottom_left = $i == count($grid) - 1 && $k == 0;
                $bottom_right = $i == count($grid) - 1 && $k == count($grid[count($grid) - 1]) - 1;

                if(!$stuck_corners || ($stuck_corners && !$top_left && !$top_right && !$bottom_left && !$bottom_right))
                {
                    $num_neighbors_on = count_neighbors_on($i, $k, $grid);

                    if($v == '#')// on
                    {
                        $new_grid[$i][$k] = $num_neighbors_on === 2 || $num_neighbors_on === 3 ? '#' : '.';
                    }
                    else// off
                    {
                        $new_grid[$i][$k] = $num_neighbors_on === 3 ? '#' : '.';
                    }
                }
